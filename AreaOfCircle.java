public class AreaOfCircle {

	public static void main(String[] args) {
	//Declare variables for radius, area
		double radius;
		double area;
		
		//Enter Radius
		radius = 15;
		
		//Compute Area
		area = radius * radius * 3.14159;
		
		//Display Results
		System.out.println("The area for a circle with radius of " + radius + " is " + area);
	}
}